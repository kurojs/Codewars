// Just a simple sorting usage.
// Create a function that returns the elements of the input-array / list sorted in lexicographical order.


import java.util.Arrays;

public class SortArray {

    public static int[] sortArray(int[] arr) {
        Arrays.sort(arr);
        return arr;
    }

    public static double[] sortArray(double[] arr) {
        Arrays.sort(arr);
        return arr;
    }

    public static float[] sortArray(float[] arr) {
        Arrays.sort(arr);
        return arr;
    }

    public static long[] sortArray(long[] arr) {
        Arrays.sort(arr);
        return arr;
    }

    public static String[] sortArray(String[] arr) {
        Arrays.sort(arr);
        return arr;
    }

    public static Integer[] sortArray(Integer[] arr) {
        Arrays.sort(arr);
        return arr;
    }
}

